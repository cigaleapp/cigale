import path from 'node:path';

import { Glob } from 'bun';

const projectRoot = path.join(import.meta.dir, '..');

const gitignore = await Bun.file(path.join(projectRoot, '.gitignore')).text();

function isGitignored(filepath: string): boolean {
	const rooted = path.relative(projectRoot, filepath).replaceAll('\\', '/');
	return gitignore.split('\n').some((line) => {
		if (line === '') return false;
		if (line.endsWith('/')) {
			return rooted.startsWith(line);
		} else {
			return rooted === line;
		}
	});
}

async function filepathTypeExpression(root: string, glob: string): Promise<string> {
	return await Array.fromAsync(new Glob(glob).scan({ cwd: root })).then((paths) =>
		paths
			.filter((file) => !isGitignored(path.join(root, file)))
			.map((file) => `'${file.replaceAll('\\', '/')}'`)
			.join(' | ')
	);
}

async function filepathTypesDeclarations(
	root: string,
	decls: Record<string, string[]>
): Promise<string[]> {
	return await Promise.all(
		Object.entries(decls).map(
			async ([typeName, globs]) =>
				`export type ${typeName} = ${await Promise.all(
					globs.map(async (glob) => filepathTypeExpression(root, glob))
				).then((exprs) => exprs.join(' | '))};`
		)
	);
}

async function namespacedDeclarations(
	namespace: string,
	root: string,
	decls: Record<string, string | string[]>
): Promise<string[]> {
	return [
		`export namespace ${namespace} {`,
		`export const root = "${root.replaceAll('\\', '/')}";`,
		Object.keys(decls).length > 0
			? `export type Any = ${Object.keys(decls).join(' | ')};`
			: '// export type Any = (no filepaths defined)',
		Object.keys(decls).length > 0
			? `export type Absolute<T extends Any = Any> = ${root ? '`${typeof root}/${T}`' : 'T'};`
			: '// export type Absolute<T extends Any = Any> = (no filepaths defined);',
		...(await filepathTypesDeclarations(
			root,
			Object.fromEntries(
				Object.entries(decls).map(([typeName, globOrGlobs]) => [
					typeName,
					Array.isArray(globOrGlobs) ? globOrGlobs : [globOrGlobs],
				])
			)
		)),
		'};',
	];
}

Bun.file(path.join(projectRoot, 'tests', 'filepaths.ts')).write(
	[
		'// This file is autogenerated by file://./../scripts/codegen-filepath-types.ts',
		...(await namespacedDeclarations('FixturePaths', 'tests/fixtures', {
			Exports: 'exports/*.zip',
			DatabaseDumps: 'db/*.devalue',
			Photos: ['*.{jpeg,jpg,png,cr2}', 'real/entomoscope/*.{jpeg,jpg,json}'],
		})),
		...(await namespacedDeclarations('ResultsPaths', 'tests/results', {})),
		...(await namespacedDeclarations('ExamplePaths', 'examples', {
			Protocols: '*.{json,yaml}',
		})),
		...(await namespacedDeclarations('RealPaths', '', {
			Protocols: 'protocols/*.{json,yaml}',
		})),
	].join('\n')
);
