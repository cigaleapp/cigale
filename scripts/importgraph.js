// build a graph of imports, replacing $lib with ../src/lib in all paths

import { readFile, writeFile } from 'fs/promises';
import { join } from 'path';
import pkg from '../package.json' assert { type: 'json' };

/**
 * @type {Map<string, Array<file: string; lineno: number, dynamic: boolean>>}
 */
let imports = new Map();

async function importsOf(filepath, provenance) {
	if (filepath.endsWith('.test.js', '.test.svelte.js')) {
		return;
	}
	if (filepath.endsWith('\\$app\\paths')) return;
	if (imports.has(filepath)) {
		return;
	}

	let content = '';
	try {
		content = await readFile(filepath, 'utf-8');
	} catch (error) {
		throw new Error(`Can't read ${filepath} imported by ${provenance.file}:${provenance.lineno}`);
	}
	const lines = content.split('\n');
	const result = [];
	let lineno = 0;
	for (const line of lines) {
		if (line.startsWith('//') || line.startsWith('#')) continue;
		lineno++;
		if (line.includes('@import')) continue;
		const match =
			line.match(/import\s+.*?\s+from\s+['"]([^'"]+)['"]/) ??
			line.match(/import\s+['"]([^'"]+)['"]/);
		const dynamicMatch = line.match(/await import\(\s*['"]([^'"]+)['"]\s*\)/);
		if (match ?? dynamicMatch) {
			let importPath = (match ?? dynamicMatch)[1];
			if (
				[...Object.keys(pkg.dependencies), ...Object.keys(pkg.devDependencies)].some(
					(dep) => importPath.startsWith(`${dep}/`) || importPath === dep
				)
			) {
				continue;
			}
			if (importPath.startsWith('$lib/')) {
				importPath = join(import.meta.dirname, '../src/lib', importPath.slice(5));
			} else {
				importPath = join(filepath, '..', importPath);
			}
			console.log(
				((match ?? dynamicMatch)[1]).padStart(40, ' '),
				'at',
				filepath + ':' + lineno,
				!match ? '(dynamic)' : ''
			);
			result.push({ file: importPath, lineno, dynamic: Boolean(!match && dynamicMatch) });
		}
	}

	for (const { file, lineno } of result) {
		await importsOf(file, { file: filepath, lineno });
	}

	imports.set(filepath, result);
}

await importsOf(join(import.meta.dirname, '../src/web-worker/index.js'), {
	file: '.',
	lineno: 0
});

let dotgraph = `digraph imports {
    // Generated by scripts/importgraph.js
    // This file is not meant to be edited
    graph [rankdir="LR"];
    node [shape="box"];
`;

let allfilesset = new Set([
	...imports.keys(),
	...[...imports.values()].flatMap((v) => v.map((i) => i.file))
]);
let allFiles = [...allfilesset];
let commonprefix = allFiles.reduce((common, file) => {
	if (!common) return file;
	let i = 0;
	while (i < common.length && i < file.length && common[i] === file[i]) {
		i++;
	}
	return common.slice(0, i);
}, allFiles[0]);

const pretty = (filepath) => filepath.replace(commonprefix, '').replace(/\\/g, '/');

for (let [file, importeds] of imports) {
	dotgraph += `    "${file}" [label="${pretty(file)}"];\n`;
	for (const { file: importedFile, lineno, dynamic } of importeds) {
		dotgraph += `    "${file}" -> "${importedFile}" [label="${lineno}"${dynamic ? ', style="dashed"' : ''}];\n`;
	}
}

dotgraph += '}\n';

const outputFile = join(import.meta.dirname, '../scripts/importgraph.dot');

await writeFile(outputFile, dotgraph, 'utf-8');
